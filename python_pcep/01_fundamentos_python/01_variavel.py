# Introduçao ao uso de variaveis

# Uma variavel é uma espaço em memoria
# que usamos para guardar algum tipo de informaçao/dado


# Atribuindo uma variavel
a = 1
b = 2
print(a)
print(b)

print(a + b)

# variavel tipo texto

texto = 'todo texto é uma string'
texto2 = 'todo texto é uma string'
print(texto)
print(texto2)

# varificando tipo de variavel
print(a, type(a))
print(texto, type(texto))


# variavel tipo float
decimal = 1.0923
print('Valor decimal', decimal)
print(type(decimal))


